<p class="style7 font13 tline mr10 pd10 bold"> 과학기술대학 교육목표</p>
<p class="pd20 tline style6">1989년 홍익대학교 조치원 캠퍼스 설립과 함께 9개 학과로 구성되어 출발한 과학기술대학은 지속적은 발전을 거듭하며 학문적 분류를 떠나 현실적인 산업구조와 특성화에 맞추어 2개학부와 5개학과로 구성되어 현재에 이르며, 홍익대학교 과학기술대학의 교육목표는 학생의 눈높이에 맞는 교육, 학생의 미래를 생각하는 교육, 교육수요자인 사회의 요구에 부응하는 교육을 제공하는 것을 본 대학의 사명으로 하여 다음과 같은 세부 목표를 갖는다. 아래 표는 과학기술대학 교육목표를 설명하고 있다.</p>
