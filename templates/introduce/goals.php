<p class="pd20 tline style6">본 학과에서는 이러한 교육과 인재양성을 효율적으로 지원하기 위한 소프트웨어, 네트워크, 통신 및 신호처리 분야의 전문교과목들(이론, 실험, 설계)을 개설하고 있으며, 본 학과의 전공교육과정은 소프트웨어와 네트워크 분야 중심의 전문 지식과 응용 능력을 갖출 뿐 아니라 양 분야의 전문지식을 융합하여 활용할 수 있는 실무 인재를 양성하는 것을 주요 목표로 하고 있다. 이러한 교육과정은 4차 산업혁명으로 가고 있는 현 사회의 수요를 반영하여 수립하였다. 졸업생들은 다양한 ICT 분야(소프트웨어, 통신 및 네트워크, 보안 등)의 산업체에 진출하거나, 학문연구를 위해 대학원 진학을 활발하게 하고 있다. 그리고  본 학과는 한국공학교육인증원(ABEEK)의 인증프로그램 심화과정을 운영하고 있다.
