<p class="mr40 pd10 font14 color5"><strong class="style9">&nbsp;&nbsp;나) 소프트웨어융합학과 프로그램의 교육목표</strong></p>
<p class="pd20 tline style6">사물 인터넷(Internet of Things: IoT), 인공지능(Artificial Intelligence: AI), 빅데이터(Big Data) 기술들이 타 산업과 융합되는 제 4차산업혁명은 미래의 삶을 혁신적으로 변화시킬 것으로 예측되고 있다. 본 소프트웨어융합학과에서는 이러한 혁명적인 산업 변화를 주도할 “실무형 인재, 융합형 인재, 창의적 인재 및 글로벌 인재” 양성을 교육목표로 하고 있다. 
  구체적으로 미래 초연결 사회의 기술 요소의 근간이 되는 소프트웨어와 네트워크 분야의 전문 지식과 응용 능력을 학생들에게 교육하여 양 분야의 전문 지식을 융합하여 활용할 수 있는 실무형 인재 양성교육, 이론적인 교육과 더불어 현실적이고 산업계 요구를 수용하는 교육을 통하여 전공 분야의 전문성을 바탕으로 자신의 발전은 물론 타 산업과의 창의적 융합을 통해 국가와 사회에 공헌할 수 있는 융합형 인재 양성교육, 개방형과제(Open Problem) 위주의 토론과 실습 위주의 수업을 통해 사용자의 근본적인 필요(Need)를 이해하고 이를 해결하는 창의적인 해결책을 제시하는 창의적 인재 양성교육에 집중하고 있다. 특히 다양한 전공수업을 영어로 개설하여, 학생들이 영어로 의사소통하며 문제를 해결할 수 있는 능력을 배양하고, 해외 유수의 대학들과의 교류 협력, 해외 인턴, 교환 학생 제도 등을 통하여 국제적 기술변화를 인지하고 다양한 해외 기술을 적극적으로 습득 활용 할 수 있는 글로벌 인재 양성교육 진행하고 있다.</p>
