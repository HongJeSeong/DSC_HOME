<p style="text-align: center;">&lt;홍익대학교 교육목표&gt;</p>
<p class="pd20 tline style6">&nbsp;&nbsp;&nbsp;&nbsp;교육목표는 교육이념과 목적을 실현하기 위해 교육현장에서 실질적으로 달성하려는 구체적 목표이다. 교육목적은 각 단위조직의 교육목표를 통제해야하며, 교육목표가 교육목적에 위배되지 않는 범위 안에서 더 구체화·전문화되어야 한다. 홍익대학교의 각 단과대학들은 홍익대학교 전체의 교육목적을 잘 반영하고 있음으로써 교육목적과 목표가 유기적 체계를 맺고 있다. </p>
